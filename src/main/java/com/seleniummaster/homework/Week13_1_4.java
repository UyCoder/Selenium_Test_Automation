package com.seleniummaster.homework;

public class Week13_1_4 {
    public static void main(String[] args) {
        int x = -5;
        int y = 10;
        if (x > 0) {
            if (y % 2 == 1)
                System.out.println(y + " is an even number");
            else
                System.out.println(y + " is an odd number");
        }
        else
            System.out.println(x);
    }

}
